public class Medico {
    String nome;
    String codigo;
    String sexo;
    String especialidade;
    Endereco endereco;
    public Medico(String nome, String codigo, String sexo, String especialidade, Endereco endereco) {
        this.nome = nome;
        this.codigo = codigo;
        this.sexo = sexo;
        this.especialidade = especialidade;
        this.endereco = endereco;
    }

}
