public class Partida {
    Time timeCasa;
    Time timeVisitante;
    int placarCasa;
    int placarVisitante;
    String estadio;

    public Partida(Time timeCasa, Time timeVisitante, int placarCasa, int placarVisitante, String estadio) {
        this.timeCasa = timeCasa;
        this.timeVisitante = timeVisitante;
        this.placarCasa = placarCasa;
        this.placarVisitante = placarVisitante;
        this.estadio = estadio;
    }

    
}
